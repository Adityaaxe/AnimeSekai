// Subcription box
var sbtn = document.getElementById("sbtn")
var sb = document.getElementById("sb")

sb.style.display = "none";

sbtn.onmouseover = function()
{
    sb.style.display = "block";
}
sbtn.onmouseout = function()
{
    sb.style.display = "none";
}
sb.onmouseover = function()
{
    sb.style.display = "block";
}
